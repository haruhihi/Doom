// 根据六爻组合查找卦
export function findHexagram(lines: number[]): IHexagram | undefined {
  return hexagrams.find(h =>
    h.lines.length === lines.length &&
    h.lines.every((v, i) => v === lines[i])
  )
}

// 根据卦序查找
export function getHexagramByNumber(num: number): IHexagram | undefined {
  return hexagrams.find(h => h.number === num)
}
